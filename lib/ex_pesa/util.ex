defmodule ExPesa.Util.Records do
  @moduledoc """
  Extracts all records information from an Erlang file.
  Returns a keyword list of {record_name, fields} tuples where record_name is the name of an extracted record and fields is a list of {field, value} tuples representing the fields for that record.
  """

  require Record
  import Record, only: [defrecord: 2, extract: 2]

  @public_key "public_key/include/public_key.hrl"

  defrecord :Certificate, extract(:Certificate, from_lib: @public_key)
  defrecord :TBSCertificate, extract(:TBSCertificate, from_lib: @public_key)
  defrecord :SubjectPublicKeyInfo, extract(:SubjectPublicKeyInfo, from_lib: @public_key)
end

